package org.bithon.agent.instrumentation.aop.interceptor.expression;

import org.antlr.v4.runtime.BaseErrorListener;
import org.antlr.v4.runtime.RecognitionException;
import org.antlr.v4.runtime.Recognizer;

import java.util.Locale;

/**
 * @author frank.chen021@outlook.com
 * @date 2023/3/26 19:43
 */
class ErrorListener extends BaseErrorListener {
    private final String expression;

    public ErrorListener(String expression) {
        this.expression = expression;
    }

    @Override
    public void syntaxError(Recognizer<?, ?> recognizer,
                            Object offendingSymbol,
                            int line,
                            int charPositionInLine,
                            String msg,
                            RecognitionException e) {
        throw new RuntimeException(String.format(Locale.ENGLISH, "%s %d %d %s", expression, line, charPositionInLine, msg));
    }
}
